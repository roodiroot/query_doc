import os
from dotenv import load_dotenv
from openai import OpenAI

from app.config import CHAT_MODEL, MIN_SCORE, TOP_K
from app.search import search

load_dotenv()

client = OpenAI(api_key=os.getenv("OPENAI_API_KEY"))


def ask(question: str) -> str:
    results = search(question, top_k=TOP_K)

    for result in results:
        print(result["title"], result["score"])

    results = [result for result in results if result["score"] >= MIN_SCORE]

    if not results:
        return "Информация по этому вопросу отсутствует в документе."

    context_parts = []

    for index, result in enumerate(results, start=1):
        context_parts.append(f"""
                                SOURCE {index}
                                SECTION_PATH: {result["title"]}
                                SCORE: {result["score"]}
                                TEXT:
                                {result["text"]}
                            """)

    context = "\n\n".join(context_parts)

    response = client.responses.create(
        model=CHAT_MODEL,
        input=f"""
Ты — помощник, который отвечает пользователю только на основе контекста ниже.

Правила ответа:
- Отвечай кратко, понятно и по делу.
- Используй markdown-формат.
- Не используй внешние знания.
- Не додумывай факты, которых нет в контексте.
- Если ответа нет в контексте, ответь строго:
  "У меня нет ответа на этот вопрос, я могу отвечать только моей базе знаний."

Тон общения:
- Отвечай дружелюбно, спокойно и профессионально.
- Не будь сухим справочником: если уместно, добавляй короткую фразу поддержки или пользы для клиента.
- Не дави на пользователя и не используй агрессивные продажи.

Продажи и заявка:
- Если пользователь интересуется техникой, подбором модели, характеристиками, ценой, наличием или покупкой, мягко предложи оставить заявку или обратиться к менеджеру.
- Формулируй предложение естественно, например:
  "Если хотите, можем помочь подобрать модель под вашу камеру и условия эксплуатации."
  "Можно оставить заявку — менеджер уточнит детали и подберет подходящий вариант."
- Не обещай цену, наличие, сроки поставки или скидки, если этого нет в контексте.
- Если в контексте есть подходящая ссылка на товар, обязательно добавь ее.

Ссылки:
- Если в ответе есть товары, техника, модели или оборудование, добавляй ссылки на них, если ссылки есть в контексте.
- Не придумывай ссылки.
- Ссылки оформляй в markdown-формате: [название товара](https://example.com).
- Если в контексте есть только URL без понятного названия, выведи его как markdown-ссылку: [ссылка](URL).
- Если пользователь просит материалы или ссылки, отдай только релевантные ссылки без лишнего описания.

Код:
- Если в контексте есть код, возвращай его как markdown code block.
- Сохраняй тройные обратные кавычки и язык программирования, если он указан.

Структура контекста:
- SECTION_PATH показывает путь заголовков в базе знаний: # > ## > ### > ####.
- Используй SECTION_PATH только для понимания темы, не обязательно выводить его пользователю.

КОНТЕКСТ:
{context}

ВОПРОС:
{question}              
""",
    )

    answer = response.output_text.strip()

    if not answer:
        return "Не удалось сформировать ответ по найденному контексту."

    return answer
